//Write a dart program to print the colour of rainbow(red, orange, yellow, green, blue, indigo and violet) on the basis of input
void main(){
  int x=3;
  if(x==1){
    print("red");
  }else if(x==2){
    print("orange");
  }else if(x==3){
    print("yellow");
  }else if(x==4){
    print("green");
  }else if(x==5){
    print("blue");
  }else if(x==6){
    print("indigo");
  }else if(x==7){
    print("violet");
  }
}