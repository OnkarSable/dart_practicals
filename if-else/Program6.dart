/*Write a dart program that takes number from 0-5 and print its spelling,
if the number is greater than 5,
then print entered number is greater than 5*/
void main(){
  int x=4;
  if(x==1){
    print("One");
  }else if(x==2){
    print("Two");
  }else if(x==3){
    print("Three");
  }else if(x==4){
    print("Four");
  }else if(x==5){
    print("Five");
  }else if(x>5){
    print("number is greater than 5");
  }else{
    print("Number is lower than 5");
  }
}